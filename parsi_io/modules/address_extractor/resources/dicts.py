def countriesCSV_to_dict():
    with open("countries.csv", "r", encoding="utf-8") as f:
        lines = [line.replace("\n", "").strip().split(",") for line in f.readlines()]
    countries = {}
    for line in lines:
        if line[1] == "0":
            countries[line[0]] = "0"
        elif line[1] == "1":
            countries[line[0]] = "1"
    return countries
